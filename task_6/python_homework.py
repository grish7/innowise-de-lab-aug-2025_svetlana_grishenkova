#!Задание 6: Калькулятор (Опциональное)

# Цель: Закрепить знания input(), print(), основных операций с числами,
# конструкции if-else, приведение типов. Научиться самостоятельно
# декомпозировать и решать задачу.
# Описание задания: Напишите программу, которая работает как простой
# калькулятор. Программа должна запросить у пользователя два числа и
# символ операции (+, -, *, /), а затем выполнить расчёт и вывести результат.
# В описании представлены минимальные требования, а корректность и
# полнота реализации зависят от степени вашего самостоятельного
# анализа задачи.



try:
    # Получаем первое число от пользователя
    first_number = float(input("Enter first number: "))  
    
    # Получаем оператор от пользователя
    symbol = input("Enter the symbol to calculate (+,-,*,/): ")  
    
    # Получаем второе число от пользователя
    second_number = float(input("Enter second number: "))
    
except ValueError:
    # Выводим ошибку
    print("Error! Please, enter valid numbers (e.g. 5, 3.14, -2.5)")
    exit()  # Завершаем программу при ошибке



# Инициализируем переменную для результата
result = 0

# Обрабатываем различные математические операции
if symbol == "+":
    # Сложение
    result = first_number + second_number
    print(result)
elif symbol == "-":
    # Вычитание
    result = first_number - second_number
    print(result)
elif symbol == "*":
    # Умножение
    result = first_number * second_number
    print(result)
elif symbol == "/":
    # Деление: проверяем, не является ли делитель нулем
    if second_number != 0:
        result = first_number / second_number
        print(result)
    else:
        # Если делитель равен нулю - выводим ошибку
        print(f"You can't divide by {second_number}")
else:
    # Если введен неподдерживаемый символ операции
    print("You entered an incorrect symbol, Please select(+,-,* or /).")


