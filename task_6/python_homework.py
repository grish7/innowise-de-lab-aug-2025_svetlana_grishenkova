# Импортируем модуль random для генерации случайных чисел
import random

# Генерируем случайное число от 1 до 5 
random_number = random.randint(1, 5)

# print(random_number)  - Выводим сгенерированное случайное число (для отладки)

# Получаем число от пользователя и преобразуем в целое
value = int(input("Enter number from 1 to 5: "))

# Проверяем, угадал ли пользователь число
if value == random_number:
    print("Correct!")
elif value > random_number:
    print("Too much!")
else:
    print("Too little!")


