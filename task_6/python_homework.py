# Задание 3: Конвертер температур

# Цель: Научиться работать с числами (тип float), применять
# математические формулы в коде и форматировать итоговый вывод.
# Описание задания: Напишите программу, которая запрашивает у
# пользователя температуру в градусах Цельсия, переводит её в градусы
# Фаренгейта и выводит результат на экран.

# Получаем температуру в градусах Цельсия от пользователя
celsius = float(input("Enter temperature in degrees Celsius: "))

# Конвертируем в Фаренгейты по формуле: (°C × 9/5) + 32
fahrenheit = (celsius * 9/5) + 32

# Форматируем результат с округлением до одного знака после запятой
result = f"{celsius} °C = {fahrenheit:.1f} °F"

# Выводим результат
print(result)


